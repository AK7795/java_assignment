public class assign1_1 {
    public static void main(String[] args) {

        // Swap 2 numbers
        int a = 2;
        int b = 4;
        System.out.println(a);
        System.out.println(b);
        int temp = a;
        a = b;

        b = temp;
        System.out.println(a);
        System.out.println(b);

    }
}
